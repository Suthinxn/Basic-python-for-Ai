input_user_one = int(input("Enter number i: "))
input_user_two = int(input("Enter number j: "))
input_magic_number = int(input("Enter magic number: "))
def magic_number(input_user_one, input_user_two, input_magic_number):
    number = "0"
    sum_str = []
    for i in range(input_user_one):
        if sum_str.append(number + str(i)):
            i += 1
            sum_str.sort()
    return sum_str

print(magic_number(input_user_one, input_user_two, input_magic_number))